package com.hanzyn.section01.arithmetic_operator;

public class Arithmetic {

	public static void main(String[] args) {
		// 산술연산자 : 사칙연산 관련 연산자.
		
		//연산 방향: 왼쪽에서 오른쪽으로 간다.
		int num1 = 20, num2 = 7;
		
		System.out.println("num1 + num2 = " + (num1 + num2));
		System.out.println("num1 - num2 = " + (num1 - num2));
		System.out.println("num1 * num2 = " + (num1 * num2));
		System.out.println("num1 / num2 = " + (num1 / num2));
		System.out.println("num1 % num2 = " + (num1 % num2));
	}

}
